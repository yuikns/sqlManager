package model;

public class TypeScoreJson {
	String count;
	String countReg;
	String cite;
	String citeLog;

	public String getCount() {
		return this.count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public String getCountReg() {
		return this.countReg;
	}

	public void setCountReg(String countReg) {
		this.countReg = countReg;
	}
	public String getCite() {
		return this.cite;
	}

	public void setCite(String cite) {
		this.cite = cite;
	}

	public String getCiteLog() {
		return this.citeLog;
	}

	public void setCiteLog(String citeLog) {
		this.citeLog = citeLog;
	}

}